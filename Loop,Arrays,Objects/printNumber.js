
// For loop Method

function printNumbers(start,end){
    for (let i = start; i < (end+1); i++){
      console.log(i)
    }
    
  }
  
  printNumbers(1, 10)



// while loop method

function printNumbers(start,end){
    let count = start-1;
    while  (count <= end -1){
      count ++;
      console.log(count);
      
    }
  }
  
  printNumbers(1, 10)






